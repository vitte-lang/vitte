#!/usr/bin/env node
import { promises as fs } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const extensionRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const repoRoot = path.resolve(extensionRoot, '..', '..');
const vitteRoot = path.join(repoRoot, 'src', 'vitte');
const outputFile = path.join(extensionRoot, 'server', 'src', 'generated', 'vitteImports.ts');

const EXCLUDED_DIRS = new Set([
  '.git',
  'node_modules',
  'out',
  'dist',
  'build',
  'target',
  '.next',
]);

function isSourceFile(filePath) {
  return filePath.endsWith('.vit') || filePath.endsWith('.vitl') || filePath.endsWith('.vitte');
}

function shouldKeepModuleName(name) {
  if (!name.startsWith('vitte/')) return false;
  if (name.includes('/tests')) return false;
  if (name.includes('/private/')) return false;
  if (name.includes('/stdlib_checked/')) return false;
  if (name.includes('/kernel_name/')) return false;
  return true;
}

async function walk(dir, out) {
  const entries = await fs.readdir(dir, { withFileTypes: true });
  for (const entry of entries) {
    if (EXCLUDED_DIRS.has(entry.name)) continue;
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      await walk(full, out);
      continue;
    }
    if (entry.isFile() && isSourceFile(full)) out.push(full);
  }
}

async function expectedContent() {
  const files = [];
  await walk(vitteRoot, files);

  const moduleNames = new Set();
  for (const file of files) {
    const text = await fs.readFile(file, 'utf8');
    const match = /^\s*space\s+([^\s]+)/m.exec(text);
    if (!match) continue;
    const mod = match[1].trim();
    if (!shouldKeepModuleName(mod)) continue;
    moduleNames.add(mod);
  }

  const sorted = Array.from(moduleNames).sort((a, b) => a.localeCompare(b));
  return [
    '// AUTO-GENERATED FILE. DO NOT EDIT MANUALLY.',
    '// Generated by: scripts/generate-vitte-imports.mjs',
    '',
    'export const VITTE_PACKAGE_IMPORTS: readonly string[] = [',
    ...sorted.map((name) => `  ${JSON.stringify(name)},`),
    '];',
    '',
  ].join('\n');
}

async function main() {
  const expected = await expectedContent();
  let current = '';
  try {
    current = await fs.readFile(outputFile, 'utf8');
  } catch {
    console.error('[imports-check] Missing generated file:', path.relative(repoRoot, outputFile));
    console.error('[imports-check] Run: npm run generate:imports');
    process.exit(1);
  }

  if (current !== expected) {
    console.error('[imports-check] Generated imports file is out of date.');
    console.error('[imports-check] Expected:', path.relative(repoRoot, outputFile));
    console.error('[imports-check] Fix by running: npm run generate:imports');
    process.exit(1);
  }

  console.log('[imports-check] OK: generated imports are in sync.');
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
